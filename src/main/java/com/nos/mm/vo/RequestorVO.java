package com.nos.mm.vo;

public class RequestorVO {
	private int req_no;
	private String req_name;
	private String req_rank;
	private String req_email;
	private String req_phone;
	private String req_note;
	private int client_no;
	
	public int getReq_no() {
		return req_no;
	}
	public void setReq_no(int req_no) {
		this.req_no = req_no;
	}
	public String getReq_name() {
		return req_name;
	}
	public void setReq_name(String req_name) {
		this.req_name = req_name;
	}
	public String getReq_rank() {
		return req_rank;
	}
	public void setReq_rank(String req_rank) {
		this.req_rank = req_rank;
	}
	public String getReq_email() {
		return req_email;
	}
	public void setReq_email(String req_email) {
		this.req_email = req_email;
	}
	public String getReq_phone() {
		return req_phone;
	}
	public void setReq_phone(String req_phone) {
		this.req_phone = req_phone;
	}
	public String getReq_note() {
		return req_note;
	}
	public void setReq_note(String req_note) {
		this.req_note = req_note;
	}
	public int getClient_no() {
		return client_no;
	}
	public void setClient_no(int client_no) {
		this.client_no = client_no;
	}
	
	
}
