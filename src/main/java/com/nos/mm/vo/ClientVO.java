package com.nos.mm.vo;

public class ClientVO {

	private int client_no;
	private String client_name;
	private String client_addr;
	private String client_start_date;
	private String client_end_date;
	private int client_sup_times;
	private String client_note;
	
	public int getClient_no() {
		return client_no;
	}
	public void setClient_no(int client_no) {
		this.client_no = client_no;
	}
	public String getClient_name() {
		return client_name;
	}
	public void setClient_name(String client_name) {
		this.client_name = client_name;
	}
	public String getClient_addr() {
		return client_addr;
	}
	public void setClient_addr(String client_addr) {
		this.client_addr = client_addr;
	}
	public String getClient_start_date() {
		return client_start_date;
	}
	public void setClient_start_date(String client_start_date) {
		this.client_start_date = client_start_date;
	}
	public String getClient_end_date() {
		return client_end_date;
	}
	public void setClient_end_date(String client_end_date) {
		this.client_end_date = client_end_date;
	}
	public int getClient_sup_times() {
		return client_sup_times;
	}
	public void setClient_sup_times(int client_sup_times) {
		this.client_sup_times = client_sup_times;
	}
	public String getClient_note() {
		return client_note;
	}
	public void setClient_note(String client_note) {
		this.client_note = client_note;
	}
	
}
